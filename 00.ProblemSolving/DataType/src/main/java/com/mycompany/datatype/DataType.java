
package com.mycompany.datatype;


public class DataType {

    public static void main(String[] args) {
        boolean b = true;
        char c = 'a';
        short s = 1523;
        int i = 56123;
        float f = 53120.651f;
        double d = 41512.4865132;
        
        System.out.printf("Boolean b = %b\n",b);
        System.out.printf("Character c = %c\n",c);
        System.out.printf("Short s = %d\n",s);
        System.out.printf("Integer i = %d\n",i);
        System.out.printf("Float f = %.1f\n",f);
        System.out.printf("Double d = %.3f\n",f);
        
        
    }
}
